def minimumAbsoluteDifference(arr):
    # greedy? 
    arr = sorted(arr)
    minimum = sys.maxsize
    for i in range(len(arr)-1):
        val = arr[i+1]-arr[i]
        if val < minimum :
            minimum = val
    return minimum

