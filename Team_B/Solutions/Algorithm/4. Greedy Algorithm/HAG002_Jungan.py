def luckBalance(k, contests):
    # Write your code here
    contests.sort(reverse=True)
    one_count = 0
    output = 0
    for c in contests:
        if c[1] == 1 : 
            one_count +=1
            if one_count <= k :
                output += c[0]
            else :
                output -= c[0]
        else:
            output += c[0]
    return output

